# create dictionary with word lists and count of words

def list_to_freq_dict(word_list):
    wordfreq = [word_list.count(p) for p in word_list]
    return dict(list(zip(word_list, wordfreq)))

# decending frequency
def dec_freq(freq_dict):
    aux = [(freq_dict[key], key) for key in freq_dict]
    aux.sort()
    aux.reverse()
    return aux


